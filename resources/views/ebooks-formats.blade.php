        <h2><a name="Ebook-File-Formats" id="Ebook-File-Formats"><strong>DIGITAL FILE FORMATS</strong></a></h2>
        <p><a name="EPUB" id="EPUB"><strong><span class="blue-text">What is the EPUB Format?</span></strong></a></p>
        <p class="rteindent1">EPUB (short for electronic publication) is a free and open eBook standard by the
            International
            Digital Publishing Forum (IDPF) that is based on XHTML, XML and CSS. EPUB eBook files have the extension .EPUB.
        </p>
        <p class="rteindent1">EPUB is designed for reflowable content, meaning that an EPUB reader can optimize text for
            a
            particular display device. EPUB also supports fixed-layout content. The format is intended as a single format
            that
            publishers and conversion houses can use in-house, as well as for distribution and sale. It is the most popular
            eBook format as it can be used on multiple devices.</p>
        <p><a name="MOBI" id="MOBI"><strong><span class="blue-text">What is the MOBI Format?</span></strong></a></p>
        <p class="rteindent1">MOBI format was invented by French company MobiPocket as an ebook format for their
            MobiPocket
            Reader application. The company was bought by Amazon and used in Amazon's Kindle eBook reader devices. MOBI
            files
            usually have .mobi extension. Because the format was originally designed for Palm Pilot PDAs, they can also have
            .prc or .pdb extension.</p>
        <p class="rteindent1">Amazon's eBooks sometimes have an .AZW extension which is an earlier version of the MOBI
            file
            format. But Amazon added DRM (Digital Rights Management) to their version of the MOBI format, which means that
            AZW
            eBooks purchased directly from Amazon can ONLY be read by Amazon's Kindle software or devices.</p>
        <p class="rteindent1">However, MOBI format files can be read on Kindles and any other MOBI-capable devices and
            software.</p>
        <p><a name="PDF-Format" id="PDF-Format"><strong><span class="blue-text">What is PDF format?</span></strong></a>
        </p>
        <p class="rteindent1">Invented by Adobe Systems in 1993, Portable Document Format (PDF) is now an open standard
            for
            electronic document exchange maintained by the International Organization for Standardization (ISO).</p>
        <p class="rteindent1">When you convert documents, forms, graphics, and web pages to PDF, they look just like
            they
            would if printed. But unlike plain printed documents, enhanced PDF files can contain clickable links and
            buttons,
            form fields, video, and audio — as well as logic to help automate routine business processes.</p>
        <p class="rteindent1">When you share a PDF file, virtually anyone can read it using the <a
                href="http://www.adobe.com/products/reader.html" target="_blank">free Adobe Reader® computer software</a> or
            the
            <a href="http://www.adobe.com/products/reader-mobile.html" target="_blank">free Adobe Reader® mobile app</a>.
        </p>
